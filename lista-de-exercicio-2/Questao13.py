dias_semana = int(input("Dias da Semana\n(1) Domingo\n(2) Segunda\n(3) Terça\n"
	"(4) Quarta\n(5) Quinta\n(6) Sexta\n(7) Sábado\nEscolha um dia: "))

if dias_semana == 1:
	print("1- Domingo!")
elif dias_semana == 2:
	print("2- Segunda!")
elif dias_semana == 3:
	print("3- Terça!")
elif dias_semana == 4:
	print("4- Quarta!")
elif dias_semana == 5:
	print("5- Quinta!")
elif dias_semana == 6:
	print("6- Sexta!")
elif dias_semana == 7:
	print("7- Sábado")
else:
	print("Informe um dia válido")